<b>But... what is this thing ?</b><br>
- This website work like a interactive book, everyone can participate to the writing process of the story.<br>
By this way, everyone can share they ideas and create a funny story, based on your unlimited imagination !<br>
<br>
<b>How can i write a post ?</b><br>
- That's pretty easy, when a post don't exist, it will be proposed to create one (only if you wanted to, of course).<br>
Then, you need to found a title (not necessary but really recommended), and some contents, after that you need to enter the number of choices the player can do.<br>
Now you just need to name/describe the choices, and link them to a new post (by default) or a old one. Easy no ? :)<br>
<br>
<b>That's awesome ! But there is some rules that i need to follow when i write something ?</b><br>
- Writing the most correctly you can, neither the language you choose.<br>
- Of course, no texts/pictures that's can break your national and navigation laws.<br>
- Considering to use <b>LESS AS POSSIBLE</b> pornographic contents, the game is created for all, and your post can be edited/deleted if your post is reported for that reason.<br>
- Think carefully about the number of choices, or your adventure will probably being hard to follow. (2 or 3 are suffisent in most cases)<br>
- Avoid to kill a part of the story by creating no choices, link to the first post or by doing a loop : Whatevers the events, anybody else can do something to continue the story logically.<br>
- Avoid as possible the "false connections" mainly when a part of the story is already well writen and logical on it progression.<br>
