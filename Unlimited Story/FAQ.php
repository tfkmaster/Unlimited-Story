<b>Mais qu'est ce que c'est que ce truc ?</b><br>
- Ceci est un site fonctionnant comme un livre intéractif, chacun peut écrire la suite de l'histoire déjà écrite pas les précédents auteurs.<br>
Ainsi chacun peut faire continuer l'histoire, en créer de nouveaux Pan, et cela à l'infinie !<br>
<br>
<b>Comment écrire un post ?</b><br>
- C'est extrèmement simple, lorsqu'un post n'existe pas, il vous sera proposer d'en créer un à votre tour (si vous le voulez bien entendu).<br>
Il vous faudra alors trouver un titre à votre post (pas nécéssaire mais très conseillé), et le fournir en contenu, puis il vous faudra choisir le nombre de possibilité qui s'offrira au joueur.<br>
Ensuite vous serez amener à nommer les choix, voir à les renvoyer vers un post déjà existant si vous le souhaitez, c'est aussi simple que cela :)<br>
<br>
<b>C'est génial ! Y a-t'il des règles à respecter pour l'écriture ?</b><br>
- Ecrire de façon comprehensible et correcte quelque soit la langue utilisé.<br>
- Bien entendu, pas de texte/image qui sortent du cadre de la loi en vigueur en France & votre pays.<br>
- Evitez le <b>PLUS POSSIBLE</b> les images/propos pornographiques, le jeu étant prévu pour être tout public, votre post pourrais être retiré/modifié si des personnes font appels pour ces raisons.<br>
- Il vaut mieux ne pas mettre trop de choix, pour que l'aventure reste agréable à jouer. (2 ou 3 suffisent en général)<br>
- Evitez de faire mourir une partie de l'histoire en ne proposant aucun choix, en revenant à l'épisode 1 ou en fermant une boucle : Quelque soit les évènements, quelqu'un d'autre pourrais faire continuer l'histoire logiquement à sa manière.<br>
- Evitez aussi autant que possible les "faux raccords" surtout lorsque un pan logique d'histoire c'est construit.<br>
<br>
<b>J'ai fait une faute dans mon post, je peut le modifier ?</b><br>
- Vous ne pouvez pas (en tout cas pour le moment).<br>
En cas de problème (En cas d'erreur, grosses fautes, report d'un post illégal/nuisible), je m'occuperais de tout (en cas de correction merci de préciser ce que vous voudriez corriger).<br>
Pour me contacter, vous pouvez envoyer un MP à "Azotik" sur Hack-Free ou par mail à cette adresse : librairiefree@altervista.org<br>
